public class Mechanic {
    public String name;
    public Double price;
    public Double lucky;
    public Double damagedProbability;

    public Mechanic(String name, Double price, Double lucky, Double damagedProbability){
        this.name=name;
        this.price=price;
        this.lucky=lucky;
        this.damagedProbability=damagedProbability;
    }
}
