public class Mechanic {
    public String name;
    public Double price;
    public Integer lucky;
    public Integer damagedProbability;

    public Mechanic(String name, Double price, Integer lucky, Integer damagedProbability){
        this.name=name;
        this.price=price;
        this.lucky=lucky;
        this.damagedProbability=damagedProbability;
    }
}
